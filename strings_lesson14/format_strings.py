#empty placeholder replace with string value

print('welcome to {} tutorials'.format('Guru99'))

#empty placeholder replace with neumeric value

print('Welcome to guru{} tutorials'.format('99'))

#using variable or keyword arguments inside placeholder

print('Welcome to {name}{value} tutorials'.format(name='guru',value='99'))

#using index or positional arguments inside placeholder

print('Welcome to {1}{0} {2} profile'.format('guru','99','club'))
